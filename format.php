          <h2 class="title">Tävlingsformat</h2>
          <p style="color:rgb(255,0,0);font-style:italic">
            Vi förbehåller oss rätten att göra finjusteringar (exempelvis rörande prispottens fördelning) och/eller förtydliganden nedan fram till tävlingens start. 
          </p>
          <h3>Allmänt</h3>
          <p>
            Tävlingen spelas över sex serier, europeiskt spelsätt. Banbyte efter varje serie.<br/>
            Dynamiskt handicap tillämpas, max 50 poäng/serie.<br/>
            Max två re-entries.<br/>
            Vid lågt antal startande garanteras spelarna att spela max två serier på samma bana.
          </p>
          <h3>Affisch</h3>
          <p>
            <a href="http://dl.dropbox.com/u/57681431/affisch2012_liten.pdf">Inbjudan</a><br/>
            <a href="http://dl.dropbox.com/u/57681431/affisch2012_stor.pdf">Högupplöst inbjudan för utskrift</a>
          </p>
          <h3>Banbehandling</h3>
          <p>
            Omoljning sker inför varje start.<br/>
            Oljeprofil: Kegel <a href="http://kegel.net/V3/PatternLibraryFunctions.aspx?OPCODE=DOWNLOADFILE&ID=621&Type=2">Beaten Path</a>.
          </p>
          <h3>Avgifter</h3>
          <p>
            Ordinarie start: 450 kr<br/>
            Re-entry: 400 kr
          </p>
          <h3>Chansen</h3>
          <p>
            Insatstävling: Tre bästa slagningarna inklusive handicap serie 4+6 vinner presentkorgar med godis. Kostnad 20:-. Lika resultat särskiljs på bäst serie 6 inklusive handicap, om lika även där jämförs övriga serier på samma sätt i ordningen (4-)5-3-2-1. Insatser i Chansen påverkar ej den progressiva potten.
          </p>
